package Elementals;

public class Alloy {

    int atomicNumber;
    String elemSym;
    String name;
    double atomicWeight;
    double meltingPoint;
    double boilingPoint;

    public Alloy(int atomicNumber, String elemSym, String name, double atomicWeight, double meltingPoint, double boilingPoint) {
        this.atomicNumber = atomicNumber;
        this.elemSym = elemSym;
        this.name = name;
        this.atomicWeight = atomicWeight;
        this.meltingPoint = meltingPoint;
        this.boilingPoint = boilingPoint;
    }


}